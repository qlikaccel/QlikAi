"""
powerbi_dataset_publisher.py
─────────────────────────────
Publishes a Power BI dataset (semantic model) from M Query expressions
generated by the Qlik → Power BI migration pipeline.

Two deployment modes — tried in priority order:
  1. XMLA / Tabular Editor 2 CLI  (PPU / Premium workspaces)
     → Full semantic model with Model View, relationships, ER diagram
  2. REST Push dataset             (any workspace)
     → Schema + relationships, no Model View; data pushed separately

Usage:
    from powerbi_dataset_publisher import publish_dataset, PublisherConfig

    config = PublisherConfig(
        workspace_id  = "your-workspace-guid",
        dataset_name  = "SalesModel",
        access_token  = "eyJ...",          # Azure AD Bearer token
    )

    result = publish_dataset(
        config       = config,
        tables       = parse_result["details"]["tables"],   # from loadscript_parser
        relationships= [],                                   # from stage 3 inference
    )

    print(result["dataset_id"])   # Power BI dataset GUID
"""

