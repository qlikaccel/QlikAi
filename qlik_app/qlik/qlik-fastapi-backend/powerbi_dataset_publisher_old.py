def _find_tabular_editor() -> Optional[str]:
    """Locate Tabular Editor 2 CLI executable."""
    import shutil
    env_path = os.getenv("TABULAR_EDITOR_PATH", "").strip()
    if env_path and os.path.isfile(env_path):
        return env_path
    candidates = [
        r"C:\Program Files\Tabular Editor 2\TabularEditor2.exe",
        r"C:\Program Files\Tabular Editor\TabularEditor.exe",
        r"C:\Program Files (x86)\Tabular Editor\TabularEditor.exe",
        "/usr/local/bin/TabularEditor2",
        "/usr/local/bin/TabularEditor",
    ]
    for p in candidates:
        if os.path.isfile(p):
            return p
    return shutil.which("TabularEditor2") or shutil.which("TabularEditor")


def _resolve_xmla_endpoint(config: PublisherConfig) -> str:
    """Resolve XMLA endpoint: env var → workspace name lookup → GUID fallback."""
    if config.xmla_endpoint:
        return config.xmla_endpoint

    try:
        resp = requests.get(
            f"{config.base_url}/groups/{config.workspace_id}",
            headers=config.rest_headers,
            timeout=10,
        )
        if resp.ok:
            workspace_name = resp.json().get("name", "").strip()
            if workspace_name:
                ep = f"powerbi://api.powerbi.com/v1.0/myorg/{workspace_name}"
                logger.info(f"[Publisher] Resolved XMLA endpoint: {ep}")
                return ep
    except Exception as e:
        logger.warning(f"[Publisher] Workspace lookup failed: {e}")

    return f"powerbi://api.powerbi.com/v1.0/myorg/{config.workspace_id}"


def _decode_token_upn(token: str) -> str:
    """Extract UPN from Bearer token for TE2 -U flag."""
    try:
        import base64 as b64
        parts = token.split(".")
        if len(parts) >= 2:
            pad    = parts[1] + "=" * (4 - len(parts[1]) % 4)
            claims = json.loads(b64.urlsafe_b64decode(pad).decode("utf-8", errors="replace"))
            return (
                claims.get("upn") or
                claims.get("preferred_username") or
                claims.get("email") or
                claims.get("unique_name") or
                "readonly"
            )
    except Exception:
        pass
    return "readonly"


def _deploy_xmla(
    config: PublisherConfig,
    bim: Dict[str, Any],
    te_path: str,
) -> Dict[str, Any]:
    """Deploy BIM via Tabular Editor 2 CLI."""
    endpoint = _resolve_xmla_endpoint(config)
    upn      = _decode_token_upn(config.access_token)

    with tempfile.NamedTemporaryFile(
        mode="w", suffix=".bim", delete=False, encoding="utf-8"
    ) as f:
        json.dump(bim, f, indent=2)
        bim_path = f.name

    try:
        cmd = [
            te_path,
            bim_path,
            "-D", endpoint, config.dataset_name,
            "-O", "-C",
            "-U", upn,
            "-P", f"Bearer {config.access_token}",
        ]
        logger.info(
            f"[Publisher/TE2] Deploying '{config.dataset_name}' to {endpoint}"
        )

        creation_flags = 0x08000008 if os.name == "nt" else 0
        try:
            proc = subprocess.run(
                cmd, capture_output=True, text=True, timeout=60,
                creationflags=creation_flags,
            )
            output = ((proc.stdout or "") + (proc.stderr or "")).strip()
            logger.info(f"[Publisher/TE2] Exit {proc.returncode}: {output[-400:]}")

            if proc.returncode == 0:
                logger.info("[Publisher/TE2] ✅ Deployment successful")
                return {
                    "success":    True,
                    "method":     "tabular_editor_cli",
                    "message":    f"Semantic model '{config.dataset_name}' deployed via TE2",
                    "cli_output": output[-400:],
                }
            return {
                "success": False,
                "method":  "tabular_editor_cli",
                "error":   output[-600:] or "No output",
            }

        except subprocess.TimeoutExpired:
            logger.warning("[Publisher/TE2] Timed out — falling back to REST XMLA")
            return {
                "success": False,
                "method":  "tabular_editor_cli",
                "status":  "te2_timeout",
                "error":   "TE2 timed out (60s)",
            }
    finally:
        try:
            os.unlink(bim_path)
        except OSError:
            pass


