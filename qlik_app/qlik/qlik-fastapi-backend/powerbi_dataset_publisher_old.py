def _pbi_col_type(qlik_type: str) -> str:
    return QLIK_TO_PBI_TYPE.get(qlik_type.lower(), "string")


def _bim_type(pbi_type: str) -> str:
    return BIM_DATA_TYPE.get(pbi_type, "string")


def build_bim(
    dataset_name:  str,
    tables:        List[Dict[str, Any]],
    relationships: List[Dict[str, Any]],
    config:        PublisherConfig,
) -> Dict[str, Any]:
    """
    Build a Tabular BIM (JSON) model definition from parsed Qlik tables.
    Uses real M expressions where available, falls back to typed placeholders.
    """
    bim_tables = []

    for table in tables:
        table_name = str(table.get("name", "")).strip()
        if not table_name:
            continue

        raw_cols = table.get("fields") or table.get("columns") or []
        bim_cols = []
        seen_cols: set = set()

        for col in raw_cols:
            if isinstance(col, str):
                col_name, qlik_type = col.strip(), "string"
            else:
                col_name  = str(col.get("name", "")).strip()
                qlik_type = col.get("type") or col.get("data_type") or "string"

            if not col_name or col_name == "*" or col_name in seen_cols:
                continue
            seen_cols.add(col_name)

            pbi_type = _pbi_col_type(qlik_type)
            bim_type = _bim_type(pbi_type)

            bim_cols.append({
                "type":         "data",
                "name":         col_name,
                "dataType":     bim_type,
                "sourceColumn": col_name,
            })

        if not table_name or not bim_cols:
            continue

        m_expr = _build_m_expression(table, config)

        bim_tables.append({
            "name":    table_name,
            "columns": bim_cols,
            "partitions": [{
                "name": f"{table_name}_Partition",
                "mode": "import",
                "source": {
                    "type":       "m",
                    "expression": m_expr,
                },
            }],
        })

    # Build relationships
    bim_rels = []
    for rel in relationships:
        from_table  = rel.get("fromTable")  or rel.get("from_table")
        from_col    = rel.get("fromColumn") or rel.get("from_column")
        to_table    = rel.get("toTable")    or rel.get("to_table")
        to_col      = rel.get("toColumn")   or rel.get("to_column")

        if not all([from_table, from_col, to_table, to_col]):
            continue

        cardinality  = str(rel.get("cardinality", "ManyToOne"))
        cross_filter = rel.get("crossFilteringBehavior", "bothDirections")

        bim_rels.append({
            "name":                   rel.get("name") or f"{from_table}_{to_table}",
            "fromTable":              from_table,
            "fromColumn":             from_col,
            "toTable":                to_table,
            "toColumn":               to_col,
            "crossFilteringBehavior": cross_filter,
            "fromCardinality":        "many" if cardinality.startswith("Many") else "one",
            "toCardinality":          "one"  if cardinality.endswith("One")   else "many",
        })

    return {
        "name": dataset_name,
        "compatibilityLevel": 1565,
        "model": {
            "culture":       "en-US",
            "tables":        bim_tables,
            "relationships": bim_rels,
            "annotations": [
                {"name": "createdBy",   "value": "QlikToPowerBI_Migrator"},
                {"name": "createdAt",   "value": datetime.now().isoformat()},
                {"name": "sourceType",  "value": "QlikLoadScript"},
            ],
        },
    }


