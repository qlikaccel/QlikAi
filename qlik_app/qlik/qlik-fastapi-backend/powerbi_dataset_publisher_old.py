class PublisherConfig:
    """Runtime configuration for PowerBI dataset deployment."""

    def __init__(
        self,
        workspace_id:       str,
        dataset_name:       str,
        access_token:       Optional[str] = None,
        xmla_endpoint:      Optional[str] = None,
        tabular_editor_path: Optional[str] = None,
        base_data_path:     str = "[DataSourcePath]",
        odbc_connection:    Optional[str] = None,
        overwrite_existing: bool = True,
    ):
        self.workspace_id        = workspace_id
        self.dataset_name        = dataset_name
        self.access_token        = access_token or os.getenv("POWERBI_ACCESS_TOKEN", "")
        self.xmla_endpoint       = xmla_endpoint or os.getenv("POWERBI_XMLA_ENDPOINT", "")
        self.tabular_editor_path = tabular_editor_path or os.getenv("TABULAR_EDITOR_PATH", "")
        self.base_data_path      = base_data_path
        self.odbc_connection     = odbc_connection
        self.overwrite_existing  = overwrite_existing
        self.base_url            = "https://api.powerbi.com/v1.0/myorg"

    @property
    def rest_headers(self) -> Dict[str, str]:
        return {
            "Authorization": f"Bearer {self.access_token}",
            "Content-Type":  "application/json",
        }


