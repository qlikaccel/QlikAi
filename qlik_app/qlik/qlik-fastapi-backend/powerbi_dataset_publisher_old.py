def _col_name_m(name: str) -> str:
    """Wrap column names with special characters in #\"…\" for M."""
    if re.match(r'^[A-Za-z_][A-Za-z0-9_ ]*$', name):
        return name
    return f'#"{name}"'


def _build_m_expression(table: Dict[str, Any], config: PublisherConfig) -> str:
    """
    Build a Power Query M expression for a table based on its source_type.

    Source types handled:
      inline   → #table with embedded rows
      qvd      → CSV fallback (QVD not natively supported in PBI)
      csv      → Csv.Document / File.Contents
      excel    → Excel.Workbook
      json     → Json.Document + Table.ExpandRecordColumn
      xml      → Xml.Tables
      resident → Table.SelectColumns reference
      sql      → Odbc.Query
      unknown  → empty typed placeholder
    """
    # If an m_expression was already generated upstream, use it directly
    if table.get("m_expression"):
        return table["m_expression"]

    src   = table.get("source_type", "unknown")
    path  = _strip_lib_prefix(table.get("source_path", ""))
    opts  = table.get("options", {})
    fields = [f for f in table.get("fields", []) if f.get("name") not in ("*", "")]

    bp = config.base_data_path

    # --- INLINE ---
    if src == "inline":
        headers = opts.get("inline_headers", [f["name"] for f in fields])
        rows    = opts.get("inline_sample", [])
        type_defs = ", ".join(
            f"{_col_name_m(h)} = {_m_annotation(f)}"
            for h, f in zip(headers, fields)
        ) or "Column1 = type text"
        row_strs = [
            "{" + ", ".join(f'"{row.get(h, "")}"' for h in headers) + "}"
            for row in rows
        ] if rows else []
        rows_block = "{\n        " + ",\n        ".join(row_strs) + "\n    }" if row_strs else "{}"
        return (
            f"let\n"
            f"    Source = #table(\n"
            f"        type table [{type_defs}],\n"
            f"        {rows_block}\n"
            f"    )\n"
            f"in\n"
            f"    Source"
        )

    # --- QVD (converted to CSV) ---
    if src == "qvd":
        csv_path = re.sub(r'\.qvd$', '.csv', path, flags=re.IGNORECASE)
        col_types = _transform_step(fields)
        return (
            f"let\n"
            f"    // QVD source: {path}\n"
            f"    // Pre-export this QVD to CSV before loading into Power BI\n"
            f"    FilePath = {bp} & \"/{csv_path}\",\n"
            f"    Source = Csv.Document(\n"
            f"        File.Contents(FilePath),\n"
            f"        [Delimiter=\",\", Encoding=65001, QuoteStyle=QuoteStyle.None]\n"
            f"    ),\n"
            f"    PromotedHeaders = Table.PromoteHeaders(Source, [PromoteAllScalars=true])"
            f"{col_types}\n"
            f"in\n"
            f"    PromotedHeaders"
        )

    # --- CSV / flat file ---
    if src == "csv":
        delim    = opts.get("delimiter", ",")
        enc      = _encoding_code(opts.get("encoding", ""))
        col_types = _transform_step(fields)
        return (
            f"let\n"
            f"    FilePath = {bp} & \"/{path}\",\n"
            f"    Source = Csv.Document(\n"
            f"        File.Contents(FilePath),\n"
            f"        [Delimiter=\"{delim}\", Encoding={enc}, QuoteStyle=QuoteStyle.Csv]\n"
            f"    ),\n"
            f"    PromotedHeaders = Table.PromoteHeaders(Source, [PromoteAllScalars=true])"
            f"{col_types}\n"
            f"in\n"
            f"    PromotedHeaders"
        )

    # --- Excel ---
    if src == "excel":
        sheet    = opts.get("sheet", "Sheet1")
        col_types = _transform_step(fields)
        return (
            f"let\n"
            f"    FilePath = {bp} & \"/{path}\",\n"
            f"    Source = Excel.Workbook(File.Contents(FilePath), null, true),\n"
            f"    SheetData = Source{{[Item=\"{sheet}\", Kind=\"Sheet\"]}}[Data],\n"
            f"    PromotedHeaders = Table.PromoteHeaders(SheetData, [PromoteAllScalars=true])"
            f"{col_types}\n"
            f"in\n"
            f"    PromotedHeaders"
        )

    # --- JSON ---
    if src == "json":
        col_names = [f'"{f["name"]}"' for f in fields]
        col_list  = ", ".join(col_names)
        col_types = _transform_step(fields)
        return (
            f"let\n"
            f"    FilePath = {bp} & \"/{path}\",\n"
            f"    Source = Json.Document(File.Contents(FilePath)),\n"
            f"    ToTable = Table.FromList(Source, Splitter.SplitByNothing(), null, null, ExtraValues.Error),\n"
            f"    Expanded = Table.ExpandRecordColumn(ToTable, \"Column1\",\n"
            f"        {{{col_list}}},\n"
            f"        {{{col_list}}}\n"
            f"    ){col_types}\n"
            f"in\n"
            f"    Expanded"
        )

    # --- XML ---
    if src == "xml":
        col_types = _transform_step(fields)
        return (
            f"let\n"
            f"    FilePath = {bp} & \"/{path}\",\n"
            f"    Source = Xml.Tables(File.Contents(FilePath)){col_types}\n"
            f"in\n"
            f"    Source"
        )

    # --- RESIDENT (reference to another query) ---
    if src == "resident":
        source_table = path or "UnknownTable"
        selected = [f['name'] for f in fields if f.get('name') not in ('*', '')]
        col_types = _transform_step(fields)
        if selected:
            select_step = (
                f",\n    Selected = Table.SelectColumns({source_table},\n"
                f"        {{{', '.join(chr(34) + c + chr(34) for c in selected)}}}\n"
                f"    )"
            )
            last = "Selected"
        else:
            select_step = ""
            last = source_table
        return (
            f"let\n"
            f"    // Derived from RESIDENT {source_table}\n"
            f"    {source_table} = {source_table}{select_step}{col_types}\n"
            f"in\n"
            f"    {last}"
        )

    # --- SQL via ODBC ---
    if src == "sql":
        conn = config.odbc_connection or "[OdbcConnectionString]"
        selected = ", ".join(f"[{f['name']}]" for f in fields if f.get('name') != '*') or "*"
        col_types = _transform_step(fields)
        return (
            f"let\n"
            f"    ConnectionString = {conn},\n"
            f"    Source = Odbc.Query(\n"
            f"        ConnectionString,\n"
            f"        \"SELECT {selected} FROM {path or 'dbo.Table'}\"\n"
            f"    ){col_types}\n"
            f"in\n"
            f"    Source"
        )

    # --- Placeholder for unknown / parquet / other ---
    type_defs = ", ".join(
        f"{_col_name_m(f['name'])} = {_m_annotation(f)}"
        for f in fields
    ) or "Column1 = type text"
    return (
        f"let\n"
        f"    // Source type '{src}' — configure FilePath manually\n"
        f"    Source = #table(type table [{type_defs}], {{}})\n"
        f"in\n"
        f"    Source"
    )


def _strip_lib_prefix(path: str) -> str:
    """Remove Qlik lib:// prefix from file paths."""
    path = re.sub(r'^lib://[^/]+/', '', path)
    path = re.sub(r'^lib://', '', path)
    return path


def _m_annotation(field: Dict) -> str:
    ftype = field.get("type", "string").lower()
    annotations = {
        "date": "type date", "datetime": "type datetime",
        "number": "type number", "integer": "type number",
        "boolean": "type logical", "string": "type text",
        "text": "type text", "mixed": "type any",
    }
    return annotations.get(ftype, "type text")


def _encoding_code(enc_str: str) -> int:
    """Map encoding name to Windows code page number."""
    enc_map = {
        "UTF-8": 65001, "UTF8": 65001,
        "UTF-16": 1200, "UTF16": 1200,
        "LATIN1": 1252, "ISO-8859-1": 28591,
    }
    return enc_map.get(enc_str.upper().replace("-", ""), 65001)


def _transform_step(fields: List[Dict]) -> str:
    """
    Build:  ,\n    TypedTable = Table.TransformColumnTypes(PromotedHeaders, {...})
    Returns '' if there are no typed fields.
    """
    typed = [f for f in fields if f.get("name") not in ("*", "") and f.get("type", "string") != "wildcard"]
    if not typed:
        return ""
    pairs = [f'{{"{f["name"]}", {_m_annotation(f)}}}' for f in typed]
    return (
        f",\n"
        f"    TypedTable = Table.TransformColumnTypes(\n"
        f"        PromotedHeaders,\n"
        f"        {{\n"
        f"        " + ",\n        ".join(pairs) + "\n"
        f"        }}\n"
        f"    )"
    )


