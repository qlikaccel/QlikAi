def publish_dataset(
    config:        PublisherConfig,
    tables:        List[Dict[str, Any]],
    relationships: Optional[List[Dict[str, Any]]] = None,
    prefer_xmla:   bool = True,
) -> Dict[str, Any]:
    """
    Publish a Power BI dataset from parsed Qlik table definitions.

    Args:
        config:        PublisherConfig with workspace_id, dataset_name, access_token
        tables:        Table list from LoadScriptParser.parse()["details"]["tables"]
        relationships: Optional relationship list from relationship inference stage
        prefer_xmla:   Try Tabular Editor 2 first (requires PPU/Premium + TE2 installed)

    Returns:
        Dict with success, dataset_id, method, message, and timing info.
    """
    start = datetime.now()
    rels  = relationships or []

    logger.info("=" * 80)
    logger.info(f"[Publisher] Starting deployment: '{config.dataset_name}'")
    logger.info(f"[Publisher] Tables: {len(tables)}  |  Relationships: {len(rels)}")
    logger.info("=" * 80)

    # Build BIM
    bim = build_bim(config.dataset_name, tables, rels, config)

    result: Optional[Dict] = None

    # ── Strategy 1: Tabular Editor 2 (XMLA) ─────────────────────────────────
    if prefer_xmla:
        te_path = config.tabular_editor_path or _find_tabular_editor()
        if te_path:
            logger.info(f"[Publisher] TE2 found at: {te_path}")

            # Delete existing if overwrite requested
            if config.overwrite_existing:
                existing_id = _lookup_dataset_id(config, config.dataset_name)
                if existing_id:
                    logger.info(f"[Publisher] Deleting existing dataset {existing_id}...")
                    if _delete_dataset(config, existing_id):
                        logger.info("[Publisher] ✅ Existing dataset deleted")
                    else:
                        logger.warning("[Publisher] ⚠️  Could not delete existing dataset")

            result = _deploy_xmla(config, bim, te_path)

            if result.get("success"):
                result["dataset_id"] = _lookup_dataset_id(config, config.dataset_name) or ""
            elif result.get("status") == "te2_timeout" or not result.get("success"):
                logger.warning("[Publisher] TE2 failed — falling back to REST Push")
                result = None
        else:
            logger.info("[Publisher] TE2 not found — using REST Push mode")

    # ── Strategy 2: REST Push dataset ────────────────────────────────────────
    if result is None or not result.get("success"):
        result = _deploy_rest(config, bim)

    # ── Finalize ─────────────────────────────────────────────────────────────
    result["tables_deployed"]       = len(bim["model"]["tables"])
    result["relationships_applied"] = len(bim["model"]["relationships"])
    result["duration_seconds"]      = round((datetime.now() - start).total_seconds(), 1)
    result["dataset_name"]          = config.dataset_name
    result["workspace_id"]          = config.workspace_id
    result["timestamp"]             = datetime.now().isoformat()
    result["bim_table_count"]       = len(bim["model"]["tables"])

    status_icon = "✅" if result.get("success") else "❌"
    logger.info(f"[Publisher] {status_icon} Deployment complete in {result['duration_seconds']}s")
    logger.info(f"[Publisher]   Method:       {result.get('method', 'N/A')}")
    logger.info(f"[Publisher]   Dataset ID:   {result.get('dataset_id', 'N/A')}")
    logger.info(f"[Publisher]   Tables:       {result['tables_deployed']}")
    logger.info(f"[Publisher]   Relationships:{result['relationships_applied']}")

    return result


