def _lookup_dataset_id(config: PublisherConfig, dataset_name: str) -> Optional[str]:
    """Find a dataset GUID by name in the target workspace."""
    try:
        resp = requests.get(
            f"{config.base_url}/groups/{config.workspace_id}/datasets",
            headers=config.rest_headers,
            timeout=15,
        )
        if resp.ok:
            for ds in resp.json().get("value", []):
                if ds.get("name") == dataset_name:
                    return ds.get("id")
    except Exception:
        pass
    return None


def _delete_dataset(config: PublisherConfig, dataset_id: str) -> bool:
    """Delete an existing dataset by ID."""
    try:
        resp = requests.delete(
            f"{config.base_url}/groups/{config.workspace_id}/datasets/{dataset_id}",
            headers=config.rest_headers,
            timeout=15,
        )
        return resp.ok
    except Exception:
        return False


