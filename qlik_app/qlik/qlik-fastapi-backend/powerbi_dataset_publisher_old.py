# Push data rows to an existing Push dataset
# ---------------------------------------------------------------------------

def push_rows_to_dataset(
    config:     PublisherConfig,
    dataset_id: str,
    table_name: str,
    rows:       List[Dict[str, Any]],
) -> Dict[str, Any]:
    """
    Push data rows into an existing Push dataset table.

    Args:
        config:     PublisherConfig (workspace_id + access_token required)
        dataset_id: GUID from publish_dataset() result
        table_name: Exact table name as defined in the dataset
        rows:       List of dicts — keys must match column names

    Returns:
        Dict with success and http_status.
    """
    url = (
        f"{config.base_url}/groups/{config.workspace_id}"
        f"/datasets/{dataset_id}/tables/{table_name}/rows"
    )
    payload = {"rows": rows}

    try:
        resp = requests.post(url, headers=config.rest_headers, json=payload, timeout=30)
        if resp.ok:
            logger.info(f"[Publisher] ✅ Pushed {len(rows)} rows to '{table_name}'")
            return {"success": True, "rows_pushed": len(rows), "http_status": resp.status_code}
        logger.error(f"[Publisher] ❌ Push rows failed ({resp.status_code}): {resp.text[:200]}")
        return {"success": False, "error": resp.text[:300], "http_status": resp.status_code}
    except Exception as exc:
        logger.exception("[Publisher] push_rows_to_dataset error")
        return {"success": False, "error": str(exc)}


# ---------------------------------------------------------------------------
# Convenience: full pipeline in one call
# ---------------------------------------------------------------------------

def publish_from_parse_result(
    parse_result:  Dict[str, Any],
    config:        PublisherConfig,
    relationships: Optional[List[Dict[str, Any]]] = None,
    prefer_xmla:   bool = True,
) -> Dict[str, Any]:
    """
    Publish directly from the output of LoadScriptParser.parse().

    Args:
        parse_result:  Dict returned by LoadScriptParser.parse()
        config:        PublisherConfig
        relationships: Optional inferred relationships
        prefer_xmla:   Use Tabular Editor if available

    Returns:
        publish_dataset() result dict.
    """
    tables = (
        parse_result.get("details", {}).get("tables", [])
        or parse_result.get("tables", [])
    )
    if not tables:
        return {
            "success": False,
            "error":   "No tables found in parse_result. Check loadscript_parser output.",
        }

    return publish_dataset(
        config        = config,
        tables        = tables,
        relationships = relationships,
        prefer_xmla   = prefer_xmla,
    )


