def _rest_type(bim_type: str) -> str:
    rest_map = {
        "string": "string", "double": "Double",
        "int64":  "Int64",  "dateTime": "DateTime", "boolean": "bool",
    }
    return rest_map.get(bim_type, "string")


def _deploy_rest(
    config:        PublisherConfig,
    bim:           Dict[str, Any],
) -> Dict[str, Any]:
    """Deploy as a REST Push dataset (works on any workspace tier)."""
    logger.info(f"[Publisher/REST] Deploying Push dataset '{config.dataset_name}'...")

    tables_raw   = bim.get("model", {}).get("tables", [])
    rels_raw     = bim.get("model", {}).get("relationships", [])

    rest_tables = []
    for t in tables_raw:
        cols = [
            {"name": c["name"], "dataType": _rest_type(c.get("dataType", "string"))}
            for c in t.get("columns", [])
        ]
        if cols:
            rest_tables.append({"name": t["name"], "columns": cols})

    rest_rels = [
        {
            "name":                   r.get("name", f"{r['fromTable']}_{r['toTable']}"),
            "fromTable":              r["fromTable"],
            "fromColumn":             r.get("fromColumn", ""),
            "toTable":                r["toTable"],
            "toColumn":               r.get("toColumn", ""),
            "crossFilteringBehavior": r.get("crossFilteringBehavior", "bothDirections"),
        }
        for r in rels_raw
        if r.get("fromTable") and r.get("toTable")
    ]

    payload: Dict[str, Any] = {
        "name":        config.dataset_name,
        "defaultMode": "Push",
        "tables":      rest_tables,
    }
    if rest_rels:
        payload["relationships"] = rest_rels

    url  = f"{config.base_url}/groups/{config.workspace_id}/datasets"
    try:
        resp = requests.post(url, headers=config.rest_headers, json=payload, timeout=30)
        if 200 <= resp.status_code < 300:
            body       = resp.json() if resp.text else {}
            dataset_id = body.get("id", "")
            logger.info(f"[Publisher/REST] ✅ Push dataset created: {dataset_id}")
            return {
                "success":       True,
                "method":        "rest_api_push",
                "dataset_id":    dataset_id,
                "message":       f"Dataset '{config.dataset_name}' created (Push mode)",
                "workspace_url": f"https://app.powerbi.com/groups/{config.workspace_id}",
            }
        logger.error(f"[Publisher/REST] ❌ Failed ({resp.status_code}): {resp.text[:300]}")
        return {
            "success": False,
            "method":  "rest_api_push",
            "error":   resp.text[:500],
        }
    except Exception as exc:
        logger.exception("[Publisher/REST] Request error")
        return {"success": False, "method": "rest_api_push", "error": str(exc)}


